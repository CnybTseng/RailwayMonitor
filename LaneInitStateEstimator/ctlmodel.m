% --- Executes just before ctlmodel is made visible.
function ctlmodel_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to ctlmodel (see VARARGIN)

% Choose default command line output for ctlmodel
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes ctlmodel wait for user response (see UIRESUME)
% uiwait(handles.figure1);

% Declearation of global variables.
global camera;
global model;
global image;

camera = varargin{1};
model  = varargin{2};

if nargin < 6
	image  = zeros(camera.yreso,camera.xreso, 3,'uint8');
else
	image = repmat(varargin{3},[1,1,3]);
end

[TX,TY,TD,nL,nw] = caltrackpoint(camera,model);

newimage = image;
for i = 1 : size(TX,1)
	newimage(TY(i),TX(i),:) = [255,255,0];
end

axes(handles.axes1);
imshow(newimage);

